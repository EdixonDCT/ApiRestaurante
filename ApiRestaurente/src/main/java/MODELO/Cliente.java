package MODELO;

public class Cliente {
    private String correo;
    private String cedula;
    private String telefono;

    public String getCorreo() {// obtener la Cedula
        return correo;
    }

    public void setCorreo(String correo) {// asginar la Cedula
        this.correo = correo;
    }

    public String getCedula() {// obtener la Cedula
        return cedula;
    }

    public void setCedula(String cedula) {// asginar la Cedula
        this.cedula = cedula;
    }

    public String getTelefono() {// obtener la Cedula
        return telefono;
    }

    public void setTelefono(String telefono) {// asginar la Cedula
        this.telefono = telefono;
    }
}
