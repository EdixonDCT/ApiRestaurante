package MODELO;

public class IngredientesCoctel {
    private String id;
    private String idIngrediente;
    private String nombreIngrediente;
    private String idCoctel;
    private String nombreCoctel;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdIngrediente() {
        return idIngrediente;
    }

    public void setIdIngrediente(String idIngrediente) {
        this.idIngrediente = idIngrediente;
    }

    public String getNombreIngrediente() {
        return nombreIngrediente;
    }

    public void setNombreIngrediente(String nombreIngrediente) {
        this.nombreIngrediente = nombreIngrediente;
    }

    public String getIdCoctel() {
        return idCoctel;
    }

    public void setIdCoctel(String idCoctel) {
        this.idCoctel = idCoctel;
    }

    public String getNombreCoctel() {
        return nombreCoctel;
    }

    public void setNombreCoctel(String nombreCoctel) {
        this.nombreCoctel = nombreCoctel;
    }
}
